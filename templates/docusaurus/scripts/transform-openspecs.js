#!/usr/bin/env node
/**
 * Transform OpenSpecs for Docusaurus
 *
 * Reads markdown files from docs/openspec/specs/ and writes them to docs-generated/specs/
 * with Docusaurus frontmatter, RFC 2119 keyword highlighting, and cross-references.
 *
 * Domain ordering is auto-discovered from directory structure (alphabetical).
 */

const fs = require('fs');
const path = require('path');
const { escapeMdxUnsafe } = require('./mdx-escape');
const {
  buildAdrMapping,
  transformRfc2119Keywords,
  transformSpecReferences,
  transformAdrReferences,
  fixMarkdownLinks,
} = require('./transform-utils');

const SPECS_SOURCE = path.join(__dirname, '../../docs/openspec/specs');
const SPECS_DEST = path.join(__dirname, '../../docs-generated/specs');

// Read baseUrl from docusaurus.config.ts
const configPath = path.join(__dirname, '../docusaurus.config.ts');
let BASE_URL = '';
if (fs.existsSync(configPath)) {
  const configContent = fs.readFileSync(configPath, 'utf-8');
  const baseUrlMatch = configContent.match(/baseUrl:\s*['"]([^'"]+)['"]/);
  BASE_URL = baseUrlMatch ? baseUrlMatch[1].replace(/\/$/, '') : '';
}

// Spec prefix to emoji and spec path mapping (generated by build-spec-mapping.js)
let SPEC_MAPPING = {};
let SPEC_EMOJIS = {};
try {
  SPEC_MAPPING = require('../src/data/spec-mapping.json');
  SPEC_EMOJIS = require('../src/data/spec-emojis.json');
} catch (e) {
  // Mapping files not yet generated
}

// ADR emoji and source path
const ADR_EMOJI = '\ud83d\udcdd';
const ADRS_SOURCE = path.join(__dirname, '../../docs/decisions');

const ADR_MAPPING = buildAdrMapping(ADRS_SOURCE);

/**
 * Auto-discover domains and build config from directory structure.
 * Generates labels from directory names: "web-dashboard" -> "Web Dashboard"
 */
function buildDomainConfig() {
  const config = {};
  if (!fs.existsSync(SPECS_SOURCE)) return config;

  const domains = fs.readdirSync(SPECS_SOURCE)
    .filter(d => fs.statSync(path.join(SPECS_SOURCE, d)).isDirectory())
    .sort();

  domains.forEach((domain, index) => {
    // Extract prefix from spec.md if possible
    let prefix = domain.toUpperCase().replace(/-/g, '').slice(0, 6);
    const specPath = path.join(SPECS_SOURCE, domain, 'spec.md');
    if (fs.existsSync(specPath)) {
      const content = fs.readFileSync(specPath, 'utf-8');
      const specMatch = content.match(/^#\s+SPEC-\d+:\s+(.+)$/m);
      if (specMatch) {
        // Use the spec title as the label
        const label = specMatch[1].trim();
        config[domain] = { order: index + 1, label: label };
        return;
      }
    }

    // Fallback: humanize the directory name
    const label = domain
      .split('-')
      .map(w => w.charAt(0).toUpperCase() + w.slice(1))
      .join(' ');
    config[domain] = { order: index + 1, label: label };
  });

  return config;
}

function extractMetadata(content, fileType) {
  let status = null;
  let date = null;

  const fmMatch = content.match(/^---\n([\s\S]*?)\n---/);
  if (fmMatch) {
    const fm = fmMatch[1];
    const statusMatch = fm.match(/^status:\s*(.+)$/m);
    const dateMatch = fm.match(/^date:\s*(.+)$/m);
    if (statusMatch) status = statusMatch[1].trim();
    if (dateMatch) date = dateMatch[1].trim();
    content = content.slice(fmMatch[0].length).replace(/^\n+/, '');
  }

  if (!status) status = fileType === 'spec' ? 'active' : 'draft';
  if (!date) date = 'unknown';

  return { status, date, content };
}

function extractTitle(content) {
  const match = content.match(/^#\s+(.+)$/m);
  return match ? match[1].trim() : 'Untitled';
}

function fixRelativePaths(content) {
  content = content.replace(/\]\(\.\.\/\.\.\/\.\.\/decisions\//g, '](../../decisions/');
  content = content.replace(/\]\(\.\.\/\.\.\/\.\.\/docs\//g, '](../../');
  return content;
}

function transformRequirementTables(content) {
  const tableRegex = /\| ID \| Requirement \|\n\|[-| ]+\|\n((?:\| [A-Z0-9-]+ \| .* \|\n)+)/g;

  return content.replace(tableRegex, (match, rows) => {
    const rowRegex = /\| ([A-Z0-9-]+) \| (.*) \|/g;
    let result = '';
    let rowMatch;

    while ((rowMatch = rowRegex.exec(rows)) !== null) {
      const id = rowMatch[1].trim();
      const text = rowMatch[2].trim();
      result += `<RequirementBox id="${id}">\n\n${text}\n\n</RequirementBox>\n\n`;
    }

    return result;
  });
}

function transformSpec(srcPath, destPath, domain, fileType, domainConfig) {
  let content = fs.readFileSync(srcPath, 'utf-8');
  const config = domainConfig[domain] || { order: 99, label: domain };

  const metadata = extractMetadata(content, fileType);
  content = metadata.content;
  const title = extractTitle(content);

  content = fixRelativePaths(content);
  content = fixMarkdownLinks(content);

  if (fileType === 'spec') {
    content = transformRequirementTables(content);
  }

  content = transformRfc2119Keywords(content);
  content = transformSpecReferences(content, { specMapping: SPEC_MAPPING, specEmojis: SPEC_EMOJIS, baseUrl: BASE_URL });
  content = transformAdrReferences(content, { adrMapping: ADR_MAPPING, adrEmoji: ADR_EMOJI, baseUrl: BASE_URL });

  const sidebarLabel = fileType === 'spec' ? 'Specification' : 'Design';
  const sidebarPosition = fileType === 'spec' ? 1 : 2;

  const metadataHeader = `
<FieldGroup>
  <Field label="Status">
    <StatusBadge status="${metadata.status.toUpperCase()}" />
  </Field>
  <Field label="Date">
    <DateBadge date="${metadata.date}" />
  </Field>
  <Field label="Domain">
    <DomainBadge domain="${config.label}" />
  </Field>
</FieldGroup>
`;

  const frontmatter = `---
title: "${title}"
sidebar_label: "${sidebarLabel}"
sidebar_position: ${sidebarPosition}
---

${metadataHeader}

`;

  fs.mkdirSync(path.dirname(destPath), { recursive: true });
  fs.writeFileSync(destPath, frontmatter + escapeMdxUnsafe(content));
}

function generateCategoryJson(destDir, domain, domainConfig) {
  const config = domainConfig[domain] || { order: 99, label: domain };

  const categoryData = {
    label: config.label,
    position: config.order,
    link: {
      type: 'generated-index',
      description: `${config.label} specifications and design documents.`
    }
  };

  const categoryPath = path.join(destDir, '_category_.json');
  fs.writeFileSync(categoryPath, JSON.stringify(categoryData, null, 2));
}

function main() {
  console.log('Transforming OpenSpecs...');

  if (!fs.existsSync(SPECS_SOURCE)) {
    console.log('  No specs directory found, skipping OpenSpec transform');
    return;
  }

  // Auto-discover domain config
  const domainConfig = buildDomainConfig();

  if (fs.existsSync(SPECS_DEST)) {
    fs.rmSync(SPECS_DEST, { recursive: true });
  }
  fs.mkdirSync(SPECS_DEST, { recursive: true });

  fs.writeFileSync(path.join(SPECS_DEST, '_category_.json'), JSON.stringify({
    label: 'Specifications',
    position: 1,
    link: {
      type: 'generated-index',
      description: 'OpenSpec specifications and design documents.'
    }
  }, null, 2));

  const domains = fs.readdirSync(SPECS_SOURCE);
  let fileCount = 0;

  for (const domain of domains) {
    const domainPath = path.join(SPECS_SOURCE, domain);
    if (!fs.statSync(domainPath).isDirectory()) continue;

    const destDomainPath = path.join(SPECS_DEST, domain);
    fs.mkdirSync(destDomainPath, { recursive: true });

    generateCategoryJson(destDomainPath, domain, domainConfig);

    const files = fs.readdirSync(domainPath);
    for (const file of files) {
      if (!file.endsWith('.md')) continue;

      const fileType = file.replace('.md', '');
      const srcPath = path.join(domainPath, file);
      const destPath = path.join(destDomainPath, file.replace(/\.md$/, '.mdx'));

      transformSpec(srcPath, destPath, domain, fileType, domainConfig);
      fileCount++;
    }
  }

  console.log(`  Transformed ${fileCount} spec files across ${Object.keys(domainConfig).length} domains`);
}

main();
